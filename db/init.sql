create table public.project
(
    id       bigint generated by default as identity primary key not null,
    name     text                                   not null,
    last_ref int                      default 0     not null,
    created  timestamp with time zone default now() not null,
    modified timestamp with time zone default now() not null
);

create table public.epic
(
    id         bigint generated by default as identity primary key not null,
    version    smallint                 default 0     not null,
    project_id bigint                                 not null,
    name       text                                   not null,
    done_tc    smallint                 default 0     not null,
    total_tc   smallint                 default 0     not null,
    completed         smallint generated always as
        (coalesce((100 * done_tc / nullif(total_tc, 0))::smallint, 0)) stored,
    created    timestamp with time zone default now() not null,
    modified   timestamp with time zone default now() not null
);

create table public.epic_history
(
    id         bigint                   not null,
    version    smallint                 not null,
    project_id bigint                   not null,
    name       text                     not null,
    modified   timestamp with time zone not null,
    deleted    boolean default false    not null,
    primary key (id, version)
);

create table public.change_phase
