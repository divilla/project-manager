create table public.change
(
    id                bigint generated by default as identity primary key not null,
    ref               integer                  default 0               not null,
    version           smallint                 default 0               not null,
    slug              text                     default ''::text        not null,
    project_id        bigint                                           not null,
    change_phase      text                     default 'backlog'::text not null,
    change_types      text[]                                           not null,
    epic_id           bigint,
    title             text                                             not null,
    body  text,
    pr_body text,
    pr_url  text,
    agent_edit        boolean                  default false           not null,
    open              boolean                  default true            not null,
    done_tc           smallint                 default 0               not null,
    total_tc          smallint                 default 0               not null,
    completed         smallint generated always as (COALESCE(
            ((((100 * done_tc) / NULLIF(total_tc, 0)))::smallint)::integer, 0)) stored,
    created           timestamp with time zone default now()           not null,
    modified          timestamp with time zone default now()           not null
);

